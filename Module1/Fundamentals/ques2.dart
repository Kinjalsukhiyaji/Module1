//2.Write a program to make addition, Subtraction, Multiplication and Division of Two Numbers.
void main()
{
  var num1=11;
  var num2=22;
  var result=0;
  result = num1+num2;
  print('Addition of $num1 and $num2 is $result');
  result = num1-num2;
  print('Subtraction of $num1 and $num2 is $result');
  result = num1*num2;
  print('Multiplication of $num1 and $num2 is $result');
  result = num1~/num2;
  print('Division of $num1 and $num2 is $result');
}